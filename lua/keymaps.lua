-- Switch between open windows.
vim.keymap.set("n", "<C-h>", "<C-w>h", { desc = "Navigate to the right window." })
vim.keymap.set("n", "<C-j>", "<C-w>j", { desc = "Navigate to the below window." })
vim.keymap.set("n", "<C-k>", "<C-w>k", { desc = "Navigate to the above window." })
vim.keymap.set("n", "<C-l>", "<C-w>l", { desc = "Navigate to the right window." })
